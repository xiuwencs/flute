    sums = 0
    for i in range(len(all_index)):
        for j in range(len(all_index[i])):
            if all_index[i][j] in count:
                count[all_index[i][j]] += 1
                sums = sums + 1
            else:
                count[all_index[i][j]] = 1
                sums = sums + 1
    # print(count)
    # 按照从小到大排序
    # print(sums)
    sorted_index = sorted(count,reverse=True)
    # print(sorted_index)

    prob_index = {}
    for i in range(len(sorted_index)):
        prob_index[sorted_index[i]] = count[sorted_index[i]]/sums
    # print(prob_index)
    return prob_index
